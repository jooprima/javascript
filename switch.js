function TestingSwitch() {
  var nilai = "joo";

  switch (nilai) {
    case 10 :
      console.log("sangat baik");
      break;
    case "joo" :
      console.log("baik");
      break;
    case 6 :
      console.log("cukup");
      break;
    default :
      console.log("lainnya");
  }
}

TestingSwitch();
